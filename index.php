            $stmt->bindParam(':offset', $offset, PDO::PARAM_INT);
            $stmt->bindParam(':recordsPerPage', $recordsPerPage, PDO::PARAM_INT);
            $stmt->execute();

            if ($stmt->rowCount() > 0) {
                while ($row = $stmt->fetch(PDO::FETCH_ASSOC)) {
